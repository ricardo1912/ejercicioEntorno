package Calculadora;

public class calcular {
	// funcion suma
	public static int suma(int num1, int num2) {
		int suma;
		suma = num1 +num2;
		return suma;
	}
	// funcion resta
	public static int resta( int num1, int num2) {
		int resta;
		resta = num1 - num2;
		return resta;
	}
	// funcion multiplicacion
	static int multiplicacion(int num1, int num2) {
		int multiplicacion;
		multiplicacion = num1 * num2;
		
		return multiplicacion;
		
	}
	
	// funcion divicion
	static int divicion(int num1, int num2) {
		int divicion;
		divicion = num1 / num2;
		
		return divicion;
	}

}
